#include <stdio.h>

int main() {
    int n;

    printf("Enter a number: ");
    scanf("%d", &n);

    printf("Name: siddh jain\n");

    if (n % 2 == 0)
        printf("Even Number\n");
    else
        printf("Odd Number\n");

    printf("Name: siddh jain\n");
    return 0;
}